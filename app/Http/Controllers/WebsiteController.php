    {
        $this->validate($request, [
            'name' => "required|string",
            'url' => "required|url",
            'name' => "required|string",
        ]);

        Website::create([
            'name' => $request->input('name'),
            'url' => $request->input('url'),
            'selector' => $request->input('selector'),
            'board_id' => $request->input('board_id'),

            'created_at' => new \DateTime(),

        ]);

        return redirect()->back();
    }

